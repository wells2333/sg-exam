<template>
  <div class="my-content-container">
    <el-row>
      <el-col :span="20" :offset="2">
        <el-table
          :key="tableKey"
          :data="examRecodeList"
          :default-sort="{ prop: 'id', order: 'descending' }"
          @cell-dblclick="handleDetail"
          highlight-current-row
          style="width: 100%;">
          <el-table-column :label="$t('exam.examinationName')" align="center">
            <template slot-scope="scope">
              <span :title="scope.row.examinationName">{{ scope.row.examinationName }}</span>
            </template>
          </el-table-column>
          <el-table-column :label="$t('exam.examinationType')" min-width="90" align="center">
            <template slot-scope="scope">
              <span>{{ transformExaminationType(scope.row.type) }}</span>
            </template>
          </el-table-column>
          <el-table-column :label="$t('exam.examinationTime')" sortable prop="start_time" min-width="90" align="center">
            <template slot-scope="scope">
              <span>{{ scope.row.startTime | fmtDate('yyyy-MM-dd hh:mm') }}</span>
            </template>
          </el-table-column>
          <el-table-column :label="$t('exam.status')" min-width="90" align="center">
            <template slot-scope="scope">
              <el-tag :type="transformStatusTag(scope.row.submitStatus, 3)">{{ transformSubmitStatus(scope.row.submitStatus)}}</el-tag>
            </template>
          </el-table-column>
          <el-table-column :label="$t('exam.score')" prop="score" align="center" width="120px">
            <template slot-scope="scope">
              <span>{{ scope.row.score }}</span>
            </template>
          </el-table-column>
          <el-table-column :label="$t('operation')" align="center">
            <template slot-scope="scope">
              <el-button type="success" size="mini" @click="handleDetail(scope.row)" :disabled="scope.row.submitStatus !== 3">{{$t('exam.scoreDetail')}}</el-button>
              <el-button type="success" size="mini" @click="handleScore(scope.row)">{{$t('exam.scoreRank')}}</el-button>
            </template>
          </el-table-column>
        </el-table>
        <el-row style="text-align: center; margin-bottom: 50px;">
          <el-col :span="24">
            <el-button v-if="!isLastPage" type="default" @click="scrollList" :loading="listLoading" style="margin-top:20px; margin-bottom: 100px;">{{$t('load.loadMore')}}</el-button>
          </el-col>
        </el-row>
      </el-col>
    </el-row>
    <el-dialog :title="$t('exam.scoreRank')" :visible.sync="dialogTableVisible">
      <el-table :data="gridData">
        <el-table-column property="rankNum" label="排名" width="150"></el-table-column>
        <el-table-column property="name" label="姓名" width="200"></el-table-column>
        <el-table-column property="score" label="分数"></el-table-column>
      </el-table>
    </el-dialog>
  </div>
</template>
<script>
import { mapState } from 'vuex'
import { fetchList } from '@/api/exam/examRecord'
import { examRankInfo } from '@/api/exam/answer'

import store from '@/store'
import { messageWarn } from '@/utils/util'

export default {
  data () {
    return {
      examRecodeList: [],
      isLastPage: false,
      total: 0,
      listLoading: true,
      tableKey: 0,
      listQuery: {
        page: 1,
        pageSize: 10,
        courseId: '',
        sort: 'id',
        order: 'descending',
        userId: ''
      },
      tempScore: {
        score: '',
        correctNumber: '',
        inCorrectNumber: ''
      },
      dialogTableVisible: false,
      gridData: []
    }
  },
  computed: {
    ...mapState({
      userInfo: state => state.user.userInfo
    })
  },
  created () {
    this.listQuery.userId = this.userInfo.id
    this.getList()
  },
  methods: {
    getList () {
      fetchList(this.listQuery).then(response => {
        const { total, isLastPage, list } = response.data.result
        this.updateList(list)
        this.total = total
        this.isLastPage = isLastPage
        this.listLoading = false
      }).catch(() => {
        messageWarn(this, this.$t('load.noMoreData'))
        this.listLoading = false
      })
    },
    scrollList () {
      if (this.isLastPage) {
        messageWarn(this, this.$t('load.noMoreData'))
        return
      }
      if (this.listLoading) {
        messageWarn(this, this.$t('load.loading'))
        return
      }
      this.listLoading = true
      setTimeout(() => {
        this.listQuery.page++
        fetchList(this.listQuery).then(response => {
          const { total, isLastPage, list } = response.data.result
          this.updateList(list)
          this.total = total
          this.isLastPage = isLastPage
          this.listLoading = false
        }).catch(() => {
          messageWarn(this, this.$t('load.loadFailed'))
          this.listLoading = false
        })
      }, 1000)
    },
    updateList (list) {
      if (list === undefined || list.length === 0) {
        return list
      }
      if (this.examRecodeList.length === 0) {
        this.examRecodeList = list
        return
      }
      list.forEach(item => {
        let exist = false
        for (let i = 0; i < this.examRecodeList.length; i++) {
          if (this.examRecodeList[i].id === item.id) {
            exist = true
          }
        }
        if (!exist) {
          this.examRecodeList.push(item)
        }
      })
    },
    handleDetail (row) {
      store.dispatch('SetIncorrectRecord', { id: row.id }).then(() => {
        this.$router.push({ name: 'incorrect', query: {recordId: row.id} })
      }).catch((error) => {
        console.error(error)
      })
    },
    handleScore (row) {
      const { examinationId } = row
      if (examinationId) {
        examRankInfo({
          examinationId
        }).then(response => {
          console.log(response, 'OK')
          const { result } = response.data
          this.gridData = result
          this.dialogTableVisible = true
        }).catch((error) => {
          console.error(error)
        })
      }
    },
    transformExaminationType (type) {
      const examType = {
        0: this.$t('status.examination'),
        1: this.$t('status.practice'),
        2: this.$t('status.questionnaire'),
        3: this.$t('status.interview')
      }
      return examType[type]
    },
    transformStatusTag (status, expectStatus) {
      return status === expectStatus ? 'success' : 'warning'
    },
    transformSubmitStatus (status) {
      const typeMap = {
        0: this.$t('status.todoMark'),
        1: this.$t('status.todoMark'),
        2: this.$t('status.markComplete'),
        3: this.$t('status.complete')
      }
      return typeMap[status]
    }
  }
}
</script>

